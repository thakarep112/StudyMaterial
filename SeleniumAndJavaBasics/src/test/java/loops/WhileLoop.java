package loops;

public class WhileLoop {

	public static void main(String[] args) {
		int i=1;
		/*
		 * While loop- When number of iterations are unknown. Entry Criteria- Condition
		 * must be satisfied. Exit Criteria- Condition must be false.
		 */
		while(i<=10) {
			
			System.out.println(i);
			i++;
		}
		
		System.out.println("Out of loop");
	}
}
